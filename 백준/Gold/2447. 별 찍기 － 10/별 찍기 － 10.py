

def star(n):
    # ----- 기본 패턴 -----------
    if n == 3:  # n이 3일 때, 가장 작은 3*3 별 모양을 리턴
        x = [['*','*','*'],
             ['*',' ','*'], 
             ['*', '*', '*']]
        return x
    
    # --- 재귀 호출 ----
    # 더 작은 패턴(크기 n//3)을 먼저 만든다
    x = star(n//3)

    # 리스트의 3배로 세로로 복제 -> 윗/중간/아래 부분 틀 잡기
    x = x*3

    # --- 윗부분 (0 ~ n//3-1) ---
    # 작은 패턴을 가로로 3배 복사
    for i in range(n//3):
        x[i] = x[i] * 3
    
    # --- 가운데 부분 (n//3 ~ 2n//3-1)
    # 좌측 패턴 + 공백(n//3) + 우측 패턴
    for i in range(n//3, (n//3)*2):
        x[i] = x[i] + [' ']*(n//3) + x[i]
    
    # --- 아랫부분 (2n//3 ~ n-1) ---
    # 작은 패턴을 가로로 3배 복사
    for i in range((n//3)*2, n):
        x[i] = x[i] * 3

    # 완성된 n*n 패턴 리턴
    return x

# 입력값 받기
n = int(input())
resultarr = star(n)  # n 크기의 패턴 생성

# 출력 (리스트를 문자열로 이어 붙임)
for i in range(n):
    print(*resultarr[i], sep='')
